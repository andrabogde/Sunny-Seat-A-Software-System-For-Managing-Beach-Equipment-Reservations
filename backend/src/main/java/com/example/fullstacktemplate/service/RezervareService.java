    /**
     * üÜï METODƒÇ NOUƒÇ: AnuleazƒÉ rezervare (fƒÉrƒÉ verificare utilizator - pentru admin)
     */
    public boolean anulaRezervare(Integer rezervareId) {
        try {
            Optional<Rezervare> rezervareOpt = getRezervareById(rezervareId);
            
            if (!rezervareOpt.isPresent()) {
                logger.warn("‚ö†Ô∏è TentativƒÉ de anulare pentru rezervarea inexistentƒÉ: {}", rezervareId);
                return false;
            }

            Rezervare rezervare = rezervareOpt.get();
            String stareVeche = rezervare.getStareRezervare();
            
            // VerificƒÉ dacƒÉ rezervarea poate fi anulatƒÉ
            if (STARE_ANULATA.equalsIgnoreCase(stareVeche)) {
                logger.warn("‚ö†Ô∏è Rezervarea {} este deja anulatƒÉ", rezervare.getCodRezervare());
                return false;
            }
            
            // SeteazƒÉ starea ca ANULATA
            rezervare.setStareRezervare(STARE_ANULATA);
            
            // SalveazƒÉ rezervarea
            Rezervare savedRezervare = rezervareRepository.save(rezervare);
            
            // üîî CreeazƒÉ notificare anulare
            createNotificationForStateChange(savedRezervare, stareVeche, STARE_ANULATA);
            
            logger.info("‚ùå Rezervare anulatƒÉ: {} (stare: {} -> {})", 
                       rezervare.getCodRezervare(), stareVeche, STARE_ANULATA);
            return true;
            
        } catch (Exception e) {
            logger.error("‚ùå Eroare la anularea rezervƒÉrii {}", rezervareId, e);
            return false;
        }
    }

